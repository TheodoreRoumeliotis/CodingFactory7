package gr.aueb.cf.ch3;

public class SumMul {
    public static void main(String[] args) {
        int i = 1;
        int sum = 0;
         while (i <= 10) {
             sum = sum + i;
             i++;





         }

        System.out.println("Το άθροισμα των πρώτων 10 αριθμών είναι: " + sum);



    }
}
